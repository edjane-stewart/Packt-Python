# create a list
shopping = ["bread","milk", "eggs"]
# print the second item in the list
print(shopping[1])
# replace the second item
shopping[1] = "banana"
print(shopping)
# print the last item in the list
print(shopping[-1])
# use slicing to print the first 2 items in the list
print(shopping[0:2]) 
# print from the beginning of the list to the third element
print(shopping[:3])
# use slicing to print list starting at the second item
print(shopping[1:])
