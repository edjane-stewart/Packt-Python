# indexing
print(">>> destination = 'San Francisco'")
destination = 'San Francisco'

print(">>> destination[0]")
print(destination[0])

print(">>> destination[1]")
print(destination[1])

print(">>> destination[2]")
print(destination[2])

print(">>> destination[-1]")
print(destination[-1])

print(">>> destination[-2]")
print(destination[-2])

print(">>> bridge = 'Golden Gate'")
bridge = 'Golden Gate'

print(">>> bridge[6]")
print(bridge[6])

# slicing
print(">>> destination[4:11]")
print(destination[4:11])

print(">>> destination[0:3]")
print(destination[0:3])

print(">>> destination[:8]")
print(destination[:8])

print(">>> destination[-3:]")
print(destination[-3:])

# Boolean
print(">>> over_18 = True")
over_18 = True

print(">>> type(over_18)")
print(type(over_18))

print(">>> over_21 = False")
over_21 = False

print(">>> type(over_21)")
print(type(over_21))