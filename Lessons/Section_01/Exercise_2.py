# determine the type of integer 6
print('>>> type(6)')
print(type(6))
print()

# determine the type of decimal number 6.0
print('>>> type(6.0)')
print(type(6.0))
print()

# determine the type of the addition below
print('>>> sum = 5 + 3.14')
sum = 5 + 3.14
print('>>> type(sum)')
print(type(sum))
print()

# convert float into integer
print('>>> n = int(7.9999999)')
n = int(7.9999999)
print('>>> print(n)')
print(n)
print()

# convert integer into float
print('>>> f = float(6)')
f = float(6)
print('>>> print(f)')
print(f)
print()