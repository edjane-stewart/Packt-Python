"""
This document determines the Pythagorean Distance
between three given points
"""

# Initialize variables
print('>>> x, y, z = 2, 3, 4')
x, y, z = 2, 3, 4
print('>>> print(x, y, z)')
print(x, y, z)
print()

# Pythagorean Theorem in 3 dimensions
print('>>> w_square = x ** 2 + y ** 2 + z ** 2')
w_square = x ** 2 + y ** 2 + z ** 2
print('>>> print(w_square)')
print(w_square)
print()

# The square root gives the distance
print('>>> w = w_square ** 0.5')
w = w_square ** 0.5
print('>>> print(w)')

# show the distance
print(w)
print()

