# Set the variable x to 14
x = 14
print(x)

# add 1 to the value of x
x += 1
print(x)

# divide x by 5 and cube it
x = (x/5) ** 2
print(x)

# playing with variable reassignments
x = 0
print('x = 0')

x += 6
print('x += 6, x contains the value ' + str(x))

x -= 2
print('x -= 2, x contains the value ' + str(x))

x *= 2
print('x *= 2, x contains the value ' + str(x))

x //= 4
print('x //= 4, x contains the value ' + str(x))

x **= 8
print('x **= 8, x contains the value ' + str(x))
